    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        if (txt_id.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, ID Barang belum diisi !");
            txt_id.requestFocus();
        } else if (txt_nm.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Nama Barang belum diisi !");
            txt_nm.requestFocus();
        } else if (txt_msk.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Tanggal Masuk Barang belum diisi !");
            txt_msk.requestFocus();
        } else if (txt_jml.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Jumlah Barang belum diisi !");
            txt_jml.requestFocus();
        } else if (txt_hst.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Harga Satuan Barang belum diisi !");
            txt_hst.requestFocus();
        } else if (txt_htl.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Harga Total Barang belum diisi !");
            txt_htl.requestFocus();
        } else if (txt_bts.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Jumlah Barang Tersedia belum diisi !");
            txt_bts.requestFocus();
        } else if (txt_bwy.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Jumlah Barang Away belum diisi !");
            txt_bwy.requestFocus();
        } else if (txt_klr.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Tanggal Keluar Barang belum diisi !");
            txt_klr.requestFocus();
        } else {
            try {
                aa.setIdBarang(txt_id.getText());
                aa.setNamaBarang(txt_nm.getText());
                aa.setTglMasuk(txt_msk.getText());
                aa.setJumlahBarang(Double.parseDouble(txt_jml.getText()));
                aa.setHargaSatuan(Double.parseDouble(txt_hst.getText()));
                aa.setHargaTotal(Double.parseDouble(txt_jml.getText()), Double.parseDouble(txt_jml.getText()));
                aa.setBarangHadir(Double.parseDouble(txt_bts.getText()));
                aa.setBarangAway(Double.parseDouble(txt_jml.getText()), Double.parseDouble(txt_bts.getText()));
                aa.setTglKeluar(txt_klr.getText());
                aa.simpanData(aa.getIdBarang(), aa.getNamaBarang(), aa.getTglMasuk(), aa.getJumlahBarang(), aa.getHargaSatuan(), aa.getHargaTotal(), aa.getBarangHadir(), aa.getBarangAway(), aa.getTglKeluar());
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan" , "Informasi", JOptionPane.INFORMATION_MESSAGE);
                tampil_database();
                reset_text();
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal tersimpan" , "Informasi",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

