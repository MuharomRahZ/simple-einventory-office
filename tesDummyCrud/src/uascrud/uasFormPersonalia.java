    public void tampil_database(){
        Object [] baris = {"ID Barang","Nama Barang","Tgl Masuk Barang","Jumlah Barang","Harga Satuan Barang","Harga Total Barang","Jumlah Barang Tersedia","Jumlah Barang Away","Tgl Keluar Barang"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_marketing.setModel(tabmode);
        try {
            hasil = aa.tampilData();
            while(hasil.next()){
                aa.setIdBarang(hasil.getString("idBarang"));
                aa.setNamaBarang(hasil.getString("namaBarang"));
                aa.setTglMasuk(hasil.getString("tglMasuk"));
//                aa.setJumlahBarang(hasil.getString("jumlahBarang"));
//                aa.setHargaSatuan(hasil.getString("hargaSatuan"));
//                aa.setHargaTotal(hasil.getString("hargaTotal"));
                aa.setJumlahBarang(hasil.getDouble("jumlahBarang"));
                aa.setHargaSatuan(hasil.getDouble("hargaSatuan"));
//                aa.setHargaTotal(hasil.getDouble("harga_total"));
                aa.setHargaTotal(hasil.getDouble("jumlahBarang"), hasil.getDouble("hargaSatuan"));
//                aa.setHargaTotal();
                aa.setBarangHadir(hasil.getDouble("barangHadir"));
                aa.setBarangAway(hasil.getDouble("jumlahBarang"), hasil.getDouble("barangHadir"));
                aa.setTglKeluar(hasil.getString("tglKeluar"));
                
//                aa.setID(hasil.getString("id"));
//                aa.setNama(hasil.getString("nama"));
//                aa.setAlamat(hasil.getString("alamat"));
                String[] data = {aa.getIdBarang(), aa.getNamaBarang(), aa.getTglMasuk(), Double.toString(aa.getJumlahBarang()), Double.toString(aa.getHargaSatuan()), Double.toString(aa.getHargaTotal()), Double.toString(aa.getBarangHadir()), Double.toString(aa.getBarangAway()), aa.getTglKeluar()};
//                String[] data = {aa.getIdBarang(), aa.getNamaBarang(), aa.getTglMasuk(), aa.getJumlahBarang(), aa.getHargaSatuan(), aa.getHargaTotal(), aa.barangHadir, aa.barangAway, aa.getTglKeluar()};
                tabmode.addRow(data);
            }
        } catch (Exception e){
            
        }
    }
    
    
